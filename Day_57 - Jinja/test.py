from flask import url_for, Flask

app = Flask(__name__)

@app.route('/')
def index():
    return 'index'

@app.route('/login')
def login():
    return 'login'

@app.route('/user/<string:username>')
def profile(username:str):
    return f'{username}\'s profile'

